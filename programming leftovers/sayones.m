function sayones(x)
    switch x;
        case 0
            fprintf('')
        case 1 
            fprintf('one')
        case 2
            fprintf('two')
        case 3 
            fprintf('three')
        case 4
            fprintf('four')
        case 5
            fprintf('five')
        case 6
            fprintf('six')
        case 7
            fprintf('seven')
        case 8
            fprintf('eight')
        case 9
            fprintf('nine')
        case 10
            fprintf('ten')
        case 11 
            fprintf('eleven')
        case 12
            fprintf('twelve')
        case 13
            fprintf('thirteen')
        case 14
            fprintf('fourteen')
        case 15
            fprintf('fifteen')
        case 16
            fprintf('sixteen')
        case 17
            fprintf('seventeen')
        case 18
            fprintf('eighteen')
        case 19
            fprintf('nineteen')
        otherwise
            fprintf('error')
    end